NLI_COVERAGE_THRESHOLD    = 0.70   # above this = concept covered
NLI_SPECIFICITY_THRESHOLD = 0.60   # above this = concept covered specifically
                                   # (lower threshold because specificity is harder to achieve)

# ── CELL 5: Scoring function ──────────────────────────────────────────────────

def score_response_v2(text, question, nli_pipe):
    """
    Hybrid scoring v2:
      1. Regex first (fast exact match)
      2. NLI coverage fallback if regex misses
      3. NLI specificity pass on ALL covered concepts

    Returns dict with:
      accuracy_score       float  final score (with guardrail)
      accuracy_score_raw   float  before guardrail
      specificity_score    float  fraction of covered concepts that are specific
      concepts_covered     list
      concepts_missed      list
      nli_rescued          list   (regex missed but NLI caught)
      specific_concepts    list   (concepts covered with high specificity)
      vague_concepts       list   (concepts covered but vaguely)
      nli_coverage_calls   int
      nli_specificity_calls int
    """
    if question not in RUBRICS or not isinstance(text, str) or not text.strip():
        return {
            "accuracy_score": 0.0, "accuracy_score_raw": 0.0,
            "specificity_score": 0.0, "concepts_covered": [],
            "concepts_missed": [], "nli_rescued": [],
            "specific_concepts": [], "vague_concepts": [],
            "nli_coverage_calls": 0, "nli_specificity_calls": 0,
        }

    rubric         = RUBRICS[question]
    total_weight   = sum(c["weight"] for c in rubric)
    covered_weight = 0.0
    covered        = []
    missed         = []
    nli_rescued    = []
    specific       = []
    vague          = []
    n_cov_calls    = 0
    n_spec_calls   = 0
    text_trunc     = text[:1024]

    for concept in rubric:
        # Step 1: regex
        regex_hit = any(re.search(p, text, re.IGNORECASE) for p in concept["regex"])

        if not regex_hit:
            # Step 2: NLI coverage fallback
            n_cov_calls += 1
            result = nli_pipe(
                sequences=text_trunc,
                candidate_labels=[concept["hyp"]],
                hypothesis_template="{}",
            )
            covered_by_nli = result["scores"][0] >= NLI_COVERAGE_THRESHOLD
        else:
            covered_by_nli = False

        is_covered = regex_hit or covered_by_nli

        if is_covered:
            covered_weight += concept["weight"]
            covered.append(concept["name"])
            if not regex_hit:
                nli_rescued.append(concept["name"])

            # Step 3: NLI specificity pass (for covered concepts only)
            n_spec_calls += 1
            spec_result = nli_pipe(
                sequences=text_trunc,
                candidate_labels=[concept["spec_hyp"]],
                hypothesis_template="{}",
            )
            is_specific = spec_result["scores"][0] >= NLI_SPECIFICITY_THRESHOLD

            if is_specific:
                specific.append(concept["name"])
            else:
                vague.append(concept["name"])
        else:
            missed.append(concept["name"])

    raw_score = covered_weight / total_weight if total_weight > 0 else 0.0

    # Guardrail
    n_covered = len(covered)
    if n_covered < 2:   raw_score *= 0.25
    elif n_covered < 3: raw_score *= 0.60

    specificity_score = len(specific) / len(covered) if covered else 0.0

    return {
        "accuracy_score":        round(raw_score, 4),
        "accuracy_score_raw":    round(covered_weight / total_weight if total_weight > 0 else 0.0, 4),
        "specificity_score":     round(specificity_score, 4),
        "concepts_covered":      covered,
        "concepts_missed":       missed,
        "nli_rescued":           nli_rescued,
        "specific_concepts":     specific,
        "vague_concepts":        vague,
        "nli_coverage_calls":    n_cov_calls,
        "nli_specificity_calls": n_spec_calls,
    }

